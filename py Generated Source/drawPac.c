void drawPac(int x, int y, int clear, int c)
{
    short int c1, c2;
    if (clear == 1)
    {
        c1 = BLACK;
        c2 = BLACK;
    }
    else
    {
        c1 = 0xff20;
        c2 = 0x83e0;
    }
    bool face = ((c % 10) >= 5);

    if (face || clear == 1)
    {
        drawPacClosed(x, y, c1, c2);
    }
    else
    {
        drawPacOpen(x, y, c1, c2);
    }
}